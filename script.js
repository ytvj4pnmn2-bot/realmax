(function initCarousel() {
  const root = document.querySelector(".rm-carousel");
  if (!root) return;

  const track = root.querySelector(".rm-carousel-track");
  const items = Array.from(root.querySelectorAll(".rm-carousel-item"));
  const btnPrev = root.querySelector(".rm-carousel-btn.prev");
  const btnNext = root.querySelector(".rm-carousel-btn.next");

  if (!track || items.length === 0) return;

  // 1 qadam = bitta kartaning kengligi + gap
  const getStep = () => {
    const itemW = items[0].getBoundingClientRect().width;
    const styles = window.getComputedStyle(track);
    const gap = parseFloat(styles.columnGap || styles.gap || "16") || 16;
    return itemW + gap;
  };

  let index = 0;
  let isPaused = false;
  let timer = null;

  const clampIndex = () => {
    // oxirigacha yetganda boshiga qaytadi
    if (index < 0) index = items.length - 1;
    if (index > items.length - 1) index = 0;
  };

  const render = () => {
    clampIndex();
    const step = getStep();
    track.style.transform = `translateX(${-index * step}px)`;
  };

  const next = () => { index += 1; render(); };
  const prev = () => { index -= 1; render(); };

  btnNext?.addEventListener("click", () => { next(); restart(); });
  btnPrev?.addEventListener("click", () => { prev(); restart(); });

  // Hover pause
  root.addEventListener("mouseenter", () => { isPaused = true; });
  root.addEventListener("mouseleave", () => { isPaused = false; });

  // Autoplay
  const start = () => {
    if (timer) clearInterval(timer);
    timer = setInterval(() => {
      if (!isPaused) next();
    }, 2500);
  };

  const restart = () => start();

  // Resize‚Äôda step o‚Äòzgaradi
  window.addEventListener("resize", render);

  render();
  start();
})();
